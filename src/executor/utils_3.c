int	ft_isnumber(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '+' || str[0] == '-')
		i++;
	while ('0' <= str[i] && str[i] <= '9')
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
